    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Filme fi = new Filme();
            Regra_Negocio_Filme vali = new Regra_Negocio_Filme();
            fi.setNome(jTextField1.getText());
            fi.setDescriçao(jTextField2.getText());
            fi.setSituacao(jComboBox2.getSelectedItem().toString());
            fi.setTipo(jComboBox2.getSelectedItem().toString());
            if (!"".equals(jTextField3.getText())) {
                try {
                    fi.setValor(Double.parseDouble(jTextField3.getText()));
                } catch (Exception e) {
                    fi.setValor(-1);
                }
            }
            vali.validarcadastro(fi);
            JOptionPane.showMessageDialog(rootPane, "Cadastro Concluido");
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

