    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Regra_Negocio_Usuario regra = new Regra_Negocio_Usuario();
        if (jComboBox1.getSelectedItem().toString().equals("Funcionario")) {
            try {
                regra.verificaAtualizacao();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        Usuario novo = new Usuario();
        try {
            novo.setNome(jTextField1.getText());
            novo.setLogin(jTextField2.getText());
            novo.setSenha(jTextField3.getText());
            novo.setTipo(jComboBox1.getSelectedItem().toString());
            regra.validarAtualizacao(novo, codigo, login);
            JOptionPane.showMessageDialog(rootPane,"Usuário atualizado");
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

