    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            filme.setNome(jTextField1.getText());
            filme.setDescriçao(jTextField2.getText());
            filme.setValor(Double.parseDouble(jTextField3.getText()));
            filme.setTipo(jComboBox2.getSelectedItem().toString());
            filme.setSituacao(jComboBox3.getSelectedItem().toString());
            Regra_Negocio_Filme regra = new Regra_Negocio_Filme();
            regra.validarAtualizacao(filme, antigo);
            JOptionPane.showMessageDialog(rootPane, "Filme Atualizado");
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

